
public class Params {
	
	String code;
	String valeur;
	
	public Params(String c, String v)
	{
		this.code = c;
		this.valeur = v;
	}
	
	public Params()
	{
		
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getValeur() {
		return valeur;
	}

	public void setValeur(String valeur) {
		this.valeur = valeur;
	}
	

}
